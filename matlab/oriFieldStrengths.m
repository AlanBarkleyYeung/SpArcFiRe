function strengths = oriFieldStrengths(ofld)
% Determines the strengths (magnitudes) of each orientation field point. 
% INPUTS:
%   ofld: MxNx2 orientation field for which the strengths are to be 
%       determined
% OUTPUT:
%   strengths: MxN matrix of strengths of each orientation field point.

strengths = sqrt(ofld(:, :, 1).^2 + ofld(:, :, 2).^2);

end